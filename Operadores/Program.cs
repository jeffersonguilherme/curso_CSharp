
namespace Operadores{
    class Program {
        static void Main(string[] args){

            // -Soma => +
            // Subtração => -
            // Multiplicação => *
            // Divisão => /


            // x += 5 é o mesmo que  x = x + 5
            double x = 0;
            x += 5; // x = x + 5
            x -= 1; // x = x - 1
            x *= 10; // x = x * 10
            x /= 2;  // x = x / 2


            // Operações Lógicas

            // E - end 
            //      Deve atender TODAS as condições
            //      &&
            // OU - or
            //      Se atender uma condição já retorna verdadeiro
            //      ||
            // Negação - not
            //      !
        }
    }
}